text = input("Type your message:\n").lower()
shift = int(input("Type the shift number:\n"))



def caeser(text1, shift1, direction):
    text2 = ''
    for letter in text1:
        if letter in alphabet:
            x = alphabet.index(letter)
            if shift1 > 26:
                print('Shift number too high')
                break
            if direction == 'encode':
                x += shift1
            else:
                x -= shift1
            
            letter2 = alphabet[x]
            text2 += letter2
        else:
            text2 += letter
    if direction == 'encode':
        print(f'The encoded text is {text2}')
    else:
        print(f'The decoded text is {text2}')


